//Vaeiaveis
int numero1 = 30;
int numero2 = 3;
Console.WriteLine($"Número 1: {numero1}");
Console.WriteLine($"Número 2: {numero2}");

//Operadores Aritméticos
//Divisão
Console.WriteLine($"Divisão: {numero1 / numero2}");

//Multiplicação
Console.WriteLine($"Multiplicação: {numero1 * numero2}");

//Subtração
Console.WriteLine($"Subtração: {numero1 - numero2}");

//Adição
Console.WriteLine($"Adição: {numero1 + numero2}");

//Módulo (Resto da Divisão)
Console.WriteLine($"Módulo: {numero1 % numero2}");

//Incrementador
Console.WriteLine($"Incrementador: {++numero1}");
/* O "++" pode ser colocado antes ou depois da variável, mas o resultado será diferente.
Se colocado antes, incrementa primeiro e depois exibe o valor.
Se colocado depois, exibe o valor e depois incrementa. */

//Decrementador
Console.WriteLine($"Decrementador: {--numero2}");
/* O "--" pode ser colocado antes ou depois da variável, mas o resultado será diferente.
Se colocado antes, incrementa primeiro e depois exibe o valor.
Se colocado depois, exibe o valor e depois incrementa. */