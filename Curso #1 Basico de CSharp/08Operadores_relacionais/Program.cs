//Variaveis
int numero1 = 5;
int numero2 = 8;

// Maior
Console.WriteLine(numero1 > numero2); // false

// Maior ou igual
Console.WriteLine(numero1 >= numero2); // false

// Menor
Console.WriteLine(numero1 < numero2); // true

// Menor ou igual
Console.WriteLine(numero1 <= numero2); // true

// Igual
Console.WriteLine(numero1 == numero2); // false

// Diferente
Console.WriteLine(numero1 != numero2); // true